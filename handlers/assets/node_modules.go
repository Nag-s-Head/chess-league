package assets

import (
	"embed"
	"log/slog"
	"net/http"
)

//go:generate pnpm i
//go:generate cp ../../node_modules/htmx.org/dist/htmx.min.js .
//go:generate pnpm build_tailwind

//go:embed htmx.min.js
//go:embed tailwind.css
//go:embed favicon.ico
var fs embed.FS

func readFile(name string) []byte {
	bytes, err := fs.ReadFile(name)
	if err != nil {
		slog.Error("Cannot read embedded file", "err", err)
		panic(err)
	}

	return bytes
}

var htmxBytes []byte = readFile("htmx.min.js")
var tailwindBytes []byte = readFile("tailwind.css")
var faviconBytes []byte = readFile("favicon.ico")

func serveAsset(bytes []byte, contentType string) func(http.ResponseWriter, *http.Request) {
	return func(w http.ResponseWriter, r *http.Request) {
		w.Header().Set("content-type", contentType)
		w.Header().Set("cache-control", "public, max-age=604800, s-max-age=604800")
		w.Write(bytes)
	}
}

func Register(mux *http.ServeMux) {
	mux.HandleFunc("/assets/htmx.js", serveAsset(htmxBytes, "application/javascript"))
	mux.HandleFunc("/assets/tailwind.css", serveAsset(tailwindBytes, "text/css"))
	mux.HandleFunc("/favicon.ico", serveAsset(faviconBytes, "image/x-icon"))
}
